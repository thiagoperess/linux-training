# Lab 1 - Comandos 

# 1 - Comando que exibe o diretório atual

pwd


# 2 - Manual de comandos ls

man ls


# 3 - Comando para acessar o diretório anterior

cd -


# 4 - Comando ou manual do palavra print

apropos print


# 5 - Limpar a tela do terminal

Ctrl+l ou clear


# 6 - Listar manuais disponíveis no diretório man

ll /usr/share/man


# 7 - Visualizar os últimos 3 comandos no histórico

fc -l -3


# 8 - Acessa o diretório raiz do sistema

cd /


# 9 - Exibir a localização do binário halt

which halt


# 10 - Reinicio da máquina para 15 minutos

shutdown -r 15
